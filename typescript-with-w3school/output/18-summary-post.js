// **Mastering TypeScript: Key Concepts I’ve Learned**
// I’ve been diving into TypeScript and wanted to share some of the key concepts that have expanded my understanding of this powerful language. Here’s a breakdown:
// ### **1. Simple Types**
// - **Explicit**: Types are defined by the developer.
// - **Implicit**: Type is inferred automatically by TypeScript.
// ### **2. Special Types**
// - **Any**: A type that can hold any value.
// - **Unknown**: A safer alternative to `any`, requiring type checking before use.
// - **Null vs Undefined**: Understanding the difference in handling null or undefined values.
// ### **3. Arrays**
// - Access elements in various ways.
// - Add or remove elements using different methods.
// ### **4. Tuples**
// - Fixed length and order matters.
// - Different types for each element, unlike arrays.
// ### **5. Objects**
// - **Interfaces**: Define the shape of an object.
// - **Optional Parameters**: Useful when you want flexibility in object properties.
// - **Destructuring**: Easily extract properties from objects.
// ### **6. Enums**
// - Ideal for storing constant values, making code more readable and manageable.
// ### **7. Type Aliases**
// - **Primitive Type Alias**
// - **Object Type Alias**
// - **Union Type Alias**
// - **Function Type Alias**
// ### **8. Functions**
// - Different types: named, anonymous, arrow, void, and more.
// - Support for **default** and **optional parameters**.
// ### **9. Casting**
// - Type casting using `as` (recommended) or `<>`.
// ### **10. Classes**
// - **Properties & Methods**: Organize and manage object behavior.
// - **Access Modifiers**: Control visibility (`public`, `private`, `protected`, `readonly`).
// - **Inheritance & Encapsulation**: Use **extends** and **implements** for reusability and protection.
// ### **11. Utility Types**
// - **Partial**: Make properties optional.
// - **Required**: Ensure all properties are mandatory.
// - **Record**: Map one type to another.
// - **Omit, Pick, ReadOnly, Exclude**: Modify types in different ways.
// ### **12. Keyof**
// - Use `keyof` to get keys of an object type.
// ### **13. Modern TypeScript Features**
// - **Nullish Coalescing (`??`)**: Handle null or undefined values.
// - **Optional Chaining (`?.`)**: Safely access deep object properties.
// - **Non-null Assertion (`!`)**: Tell TypeScript that a value is not null/undefined.
